package Posttest2.src;

public class SoloComeback {
    protected String titleTrack;
    protected String namaAlbum;
    protected String tanggalCB;
    protected String waktuCB;
    protected String nameMember;
    public SoloComeback(String titleTrack, String namaAlbum, String tanggalCB, String waktuCB, String nameMember) {
        this.titleTrack = titleTrack;
        this.namaAlbum = namaAlbum;
        this.tanggalCB = tanggalCB;
        this.waktuCB = waktuCB;
        this.nameMember = nameMember;
    }
    public String getTitleTrack() {
        return titleTrack;
    }
    public void setTitleTrack(String titleTrack) {
        this.titleTrack = titleTrack;
    }
    public String getNamaAlbum() {
        return namaAlbum;
    }
    public void setNamaAlbum(String namaAlbum) {
        this.namaAlbum = namaAlbum;
    }
    public String getTanggalCB() {
        return tanggalCB;
    }
    public void setTanggalCB(String tanggalCB) {
        this.tanggalCB = tanggalCB;
    }
    public String getWaktuCB() {
        return waktuCB;
    }
    public void setWaktuCB(String waktuCB) {
        this.waktuCB = waktuCB;
    }
    public String getNameMember() {
        return nameMember;
    }
    public void setNameMember(String nameMember) {
        this.nameMember = nameMember;
    }
    
    
}

